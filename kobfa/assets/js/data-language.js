function chageLang(lang = 'en') {
  String.i18n(document, data[lang])
  localStorage.setItem('lang', lang)

  if (lang == 'en') {
    $('#btn-en').addClass('active')
    $('#btn-th').removeClass('active')
  }

  if (lang == 'th') {
    $('#btn-en').removeClass('active')
    $('#btn-th').addClass('active')
  }
  onChangeLang(lang)
}